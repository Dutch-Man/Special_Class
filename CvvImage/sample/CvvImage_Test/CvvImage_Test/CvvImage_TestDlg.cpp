void CCvvImage_TestDlg::Draw(int ID, Mat img)
{
	HDC hdc;
	CDC *pCDC;
	CRect rect;
	pCDC = GetDlgItem(ID)->GetDC();//根据ID获得窗口指针再获取与该窗口关联的上下文指针
	hdc = pCDC->GetSafeHdc();
	GetDlgItem(ID)->GetClientRect(&rect);
	IplImage src = img; // 定义IplImage指针变量src
	CvvImage cimg;
	cimg.CopyOf(&src, src.nChannels);
	cimg.DrawToHDC(hdc, &rect);
}